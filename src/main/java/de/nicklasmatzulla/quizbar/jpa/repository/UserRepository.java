package de.nicklasmatzulla.quizbar.jpa.repository;

import de.nicklasmatzulla.quizbar.jpa.entity.UserEntity;
import org.springframework.data.repository.CrudRepository;

import java.util.List;
import java.util.Optional;

public interface UserRepository extends CrudRepository<UserEntity, Long> {

    Optional<UserEntity> findByFirstNameAndLastName(final String firstName, final String lastName);

    List<UserEntity> findTop3ByGroupSizeOrderByPointsDesc(int groupSize);

    boolean existsByFirstNameAndLastName(final String firstName, final String lastName);

}
